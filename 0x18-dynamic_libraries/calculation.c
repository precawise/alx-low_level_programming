#include "main.h"

/**
 * add - Calculates the remainder of division between two integers.
 * @a: The first integer (dividend).
 * @b: The second integer (divisor).
 * Return: The remainder of the division between the two integers.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Calculates the remainder of division between two integers.
 * @a: The first integer (dividend).
 * @b: The second integer (divisor).
 * Return: The remainder of the division between the two integers.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Calculates the remainder of division between two integers.
 * @a: The first integer (dividend).
 * @b: The second integer (divisor).
 * Return: The remainder of the division between the two integers.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Calculates the remainder of division between two integers.
 * @a: The first integer (dividend).
 * @b: The second integer (divisor).
 * Return: The remainder of the division between the two integers.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Calculates the remainder of division between two integers.
 * @a: The first integer (dividend).
 * @b: The second integer (divisor).
 * Return: The remainder of the division between the two integers.
 */
int mod(int a, int b)
{
	return (a % b);
}
